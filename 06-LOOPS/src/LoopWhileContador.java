
public class LoopWhileContador {
	
	public void loopWhile1() {
		
		int cont = 0;
		
		while (cont <= 10) {
			
			System.out.println("VALOR DO CONTADOR: " + cont);
			//cont = cont + 1;
			//cont += 1;
			cont++;
			
		}
		
	}
	
	public void loopWhile2(int limite) {
		
		int cont = 0;
		
		while (cont <= limite) {
			
			System.out.println("VALOR DO CONTADOR: " + cont);
			//cont = cont + 1;
			//cont += 1;
			cont++;
			
		}
		
	}

}
